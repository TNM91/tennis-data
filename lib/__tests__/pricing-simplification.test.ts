import { readFileSync } from 'node:fs'
import { join } from 'node:path'
import { describe, expect, it } from 'vitest'

const pricingSource = readFileSync(join(process.cwd(), 'app/pricing/page.tsx'), 'utf8')
const pricingLayoutSource = readFileSync(join(process.cwd(), 'app/pricing/layout.tsx'), 'utf8')
const joinSource = readFileSync(join(process.cwd(), 'app/join/page.tsx'), 'utf8')

describe('pricing simplification', () => {
  it('keeps pricing organized around the five decision sections', () => {
    expect(pricingSource).toContain('Choose by tennis job')
    expect(pricingSource).toContain('Start from what you are trying to do.')
    expect(pricingSource).toContain('JOB_CHOOSER')
    expect(pricingSource).toContain('Find tennis context')
    expect(pricingSource).toContain('Improve my game')
    expect(pricingSource).toContain('Captain match week')
    expect(pricingSource).toContain('Run a season or event')
    expect(pricingSource).toContain('Choose your role')
    expect(pricingSource).toContain('See the workspace')
    expect(pricingSource).toContain('Compare what unlocks')
    expect(pricingSource).toContain('Billing clarity')
    expect(pricingSource).toContain('Start free / upgrade')
  })

  it('uses public workspace naming and consistent League billing', () => {
    expect(pricingSource).toContain('Player - My Lab for your game')
    expect(pricingSource).toContain('Coach - Coach Hub for player development')
    expect(pricingSource).toContain('Captain - Team Hub for match week')
    expect(pricingSource).toContain('League - League Office for a season')
    expect(pricingSource).toContain('Full-Court - Every tennis job, including Tournament Desk')
    expect(pricingSource).toContain('Connect My Lab, Coach Hub, Team Hub, League Office')
    expect(pricingSource).toContain('Full-Court access pass')
    expect(pricingSource).toContain('All paid workspaces are active.')
    expect(pricingSource).toContain('without stale locks or repeated upgrade prompts')
    expect(pricingSource).toContain('Full-Court workspace fit proof cue')
    expect(pricingSource).toContain('Pick the right workspace for the tennis job.')
    expect(pricingSource).toContain('Full-Court role switching proof cue')
    expect(pricingSource).toContain('Prove every paid workspace opens without stale locks.')
    expect(pricingSource).toContain('FULL_COURT_ROLE_SWITCHING_PROOF')
    expect(pricingSource).toContain('Begin on Pricing with the Full-Court plan marked active')
    expect(pricingSource).toContain('Visit My Lab, Coach Hub, Team Hub, and League Office')
    expect(pricingSource).toContain('Come back to Pricing and choose the next workspace')
    expect(pricingSource).toContain('Player job')
    expect(pricingSource).toContain('Coach job')
    expect(pricingSource).toContain('Captain job')
    expect(pricingSource).toContain('League job')
    expect(pricingSource).toContain('FULL_COURT_WORKSPACE_PASS')
    expect(pricingSource).toContain('FULL_COURT_WORKSPACE_FIT_PROOF')
    expect(pricingSource).toContain("href: '/mylab'")
    expect(pricingSource).toContain("href: '/coach'")
    expect(pricingSource).toContain("href: '/captain'")
    expect(pricingSource).toContain("href: '/league-coordinator'")
    expect(pricingSource).toContain('League is $14.99 per season workspace')
    expect(pricingLayoutSource).toContain('Free, My Lab, Coach Hub, Team Hub, League Office, and Full-Court')
    expect(joinSource).toContain('Full-Court starts with a free account, then unlocks My Lab, Coach Hub, Team Hub, League Office')
    expect(joinSource).toContain('Set up every tennis job.')
    expect(joinSource).toContain('Create your account. Run every tennis job.')
    expect(joinSource).toContain('Activate Full-Court next, then open one connected tennis operation.')
    expect(joinSource).toContain('activate Full-Court to run every tennis job.')
    expect(pricingSource).not.toContain('You, Coach, Team, or League')
    expect(pricingSource).not.toContain('League is $14.99/month')
    expect(pricingSource).not.toContain('Full-Court - Everything connected, including Tournament Desk')
    expect(joinSource).not.toContain('the full suite')
    expect(joinSource).not.toContain('Create your account. Run the full court.')
    expect(joinSource).not.toContain('open the full tennis operations workspace')
    expect(`${pricingSource}\n${pricingLayoutSource}\n${joinSource}`).not.toContain('Player, Coach, Captain, League, and unlimited tournaments')
  })
})
