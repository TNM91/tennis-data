    expect(portalToolbarSource).toContain('setCurrentHash(target.hash || \'\')')
    expect(styleBlock(portalToolbarSource, 'mobilePortalPaletteStyle')).toContain("gridTemplateColumns: 'repeat(3, minmax(0, 1fr))'")
    expect(styleBlock(portalToolbarSource, 'mobilePortalActionPaletteStyle')).toContain("gridTemplateColumns: 'repeat(3, minmax(0, 1fr))'")
